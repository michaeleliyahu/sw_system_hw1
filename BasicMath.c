#include<stdio.h>
#include"myMath.h"

float add(float x, float y){
    float ad = x+y;
    return ad;
}
float sub(float x, float y){
    float su = x-y;
    return su;
}
double mul(double x, int y){
    double mu = x*y;
    return mu;
}
double div(double x, int y){
    double di = x/y;
    return di;
}
